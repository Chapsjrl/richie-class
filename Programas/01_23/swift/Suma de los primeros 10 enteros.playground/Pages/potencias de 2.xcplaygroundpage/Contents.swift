import UIKit
 
func potenciasDe2(desde numeroInicial:Int, hasta numeroTope: Int)
{
    var num=numeroInicial
    
    while num < numeroTope {
        // pow = potencia elevada
        print(pow(2,num))
        num += 1
        
    }
}

potenciasDe2(desde: 3, hasta: 15)
